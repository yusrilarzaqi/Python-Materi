a=10
b=3

print("Operasi Tambah +")
hasil=a+b
print(a,'+',b,'=',hasil)

print("Operasi kurang -")
hasil=a-b
print(a,'-',b,'=',hasil)

print("Operasi bagi /")
hasil=a/b
print(a,'/',b,'=',hasil)

print("Operasi kali *")
hasil=a*b
print(a,'*',b,'=',hasil)

print("Operasi eksponen **")
hasil=a**b
print(a,'**',b,'=',hasil)

print("Operasi floor devision //")
hasil=a//b # Dibagi lalu dibulatkan kebawa
print(a,'//',b,'=',hasil)

