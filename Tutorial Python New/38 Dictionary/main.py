# list -> array.
# mengakses cara dengan menggunakan index
data_list = ["ucup", "dudung", "otong"]

print(data_list[0])

# dictionary (dict) -> associative array
# indentifier -> key

data_dict = {
    "cp": "ucup",
    "tg": "otong",
    "dg": "dudung",
    "nmbr": 100,
    "list": data_list,
}

print(data_dict["nmbr"])
print(data_dict["list"])
